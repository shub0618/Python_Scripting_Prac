# Arithmetic Operators
# +, -, *, /, %, **.

x = 2
y = 7

# Addition
total = x + y
print(total)

# Subtraction
total = x - y
print(total)

# Multiplication
total = x * y
print(total)

# Division
total = y/x
print(total)

# Modulus or getting the reminder 
total = y % x
print(total)

# Exponential
total = y**x
print(total)
